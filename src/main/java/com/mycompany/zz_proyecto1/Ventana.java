    private void btnCrearDefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearDefActionPerformed
        String nombreD = txfNombreDef.getText().trim();
        String vida = txfVidaDef.getText().trim();
        String golpes = txfGolpesDef.getText().trim();
        String campos = txfCamposDef.getText().trim();
        String apari = txfLvlApariciónDef.getText().trim();
        String comboD = (String) cbxDefensas.getSelectedItem();
        String rango = txfLvlRangoDef.getText().trim();
        String img1Def = txfImagen1Def.getText().trim();
        int vidas = Integer.parseInt(vida);
        int golpe = Integer.parseInt(golpes);
        int campo = Integer.parseInt(campos);
        int aparis = Integer.parseInt(apari);
        int rangos = Integer.parseInt(rango);
        txaDefensasCreadas.append(nombreD + " " +comboD + " con vida: " + vidas +"\n");
        if(comboD == "De contacto" || comboD == "Aereos"){
            agregarPersonaLista(nombreD, img1Def, "", vidas, golpe, 0, campo, aparis, comboD, 1, 1);
        }else if(comboD == "Impacto"){
            agregarPersonaLista(nombreD, img1Def, "", vidas, golpe, 0, campo, aparis, comboD, 1, 1);
        }else if(comboD == "Bloques"){
            agregarPersonaLista(nombreD, img1Def, "", vidas, 0, 0, campo, aparis, comboD, 0, 1);
        }else if(comboD == "Mediano alcance"){
            agregarPersonaLista(nombreD, img1Def, "", vidas, golpe, 0, campo, aparis, comboD, rangos, 1);
        }else{
            agregarPersonaLista(nombreD, img1Def, "", vidas, golpe, 0, campo, aparis, comboD, rangos, 1);
        }
        txfImagen1Def.setText("");
        txfNombreDef.setText(""); 
        txfVidaDef.setText("100"); 
        txfGolpesDef.setText("1"); 
        txfCamposDef.setText("2");
        txfLvlRangoDef.setText("2");
        txfLvlApariciónDef.setText("0");       
    }//GEN-LAST:event_btnCrearDefActionPerformed

