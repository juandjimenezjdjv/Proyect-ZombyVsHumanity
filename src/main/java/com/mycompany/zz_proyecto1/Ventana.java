    public void agregarPersonaLista(String nombre, String img1, String img2, int vida, int golpes, int nivel, int campos, int aparicion, String Combo, int rango, int bandera){
        if (defensas.isEmpty()){
            defensas.add(new Personaje("Partida","0", "0", 2000, 0, 0, 0, 0, "0", 0));
            defensas.add(new Personaje("Reliquia","reliquia.png", "", 300, 0, 0, 0, 0, "reliquia", 0));
        }
        if (bandera == 0){
            zombies.add(new Personaje(nombre,img1, "", vida, golpes, nivel, campos, aparicion, Combo, rango));
        }else if (bandera == 1){
            defensas.add(new Personaje(nombre,img1, "", vida, golpes, nivel, campos, aparicion, Combo, rango));
        }        
    }
    
    public void guardarPartida(){
        String partida = txfNomPartida.getText();
        FileManager.writeObject(defensas, partida+"=def.ser");
        FileManager.writeObject(zombies, partida+"=Zom.ser"); 
    }
